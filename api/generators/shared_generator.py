def _readme(stack: StackSelection, project_name: str) -> str:
    frontend = stack.frontend.lower()
    backend = stack.backend.lower()
    db = stack.database.lower()

    is_nextjs_only = "next" in frontend and (not backend or backend == "none")
    is_python = "fastapi" in backend or "django" in backend
    has_docker = any("docker" in s.lower() for s in [stack.deployment] + stack.additional)

    stack_table = f"""| Layer | Choice |
|---|---|
| Frontend | {stack.frontend} |
| Backend | {stack.backend if backend and backend != "none" else "Next.js API routes"} |
| Database | {stack.database} |
| Auth | {stack.auth} |
| Deployment | {stack.deployment} |"""

    if is_nextjs_only:
        return _readme_nextjs(project_name, stack, stack_table)
    if is_python:
        return _readme_python(project_name, stack, stack_table, has_docker)
    if "mongo" in db:
        return _readme_mern(project_name, stack, stack_table, has_docker)
    return _readme_pern(project_name, stack, stack_table, has_docker)


